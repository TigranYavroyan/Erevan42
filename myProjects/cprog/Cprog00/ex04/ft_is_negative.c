#include <unistd.h>

void	ft_is_negative(int n)
{
	char	np;
	char	p;

	if (n < 0)
	{
		np = 'N';
		write(1, &np, 1);
	}
	else
	{
		p = 'P';
		write(1, &p, 1);
	}
}
